import java.util.Arrays;
class TraverseTheArray
{
	public static void main(String[] args) 
	{
		int [][] arr = {{1,2,3,4,5,6},{'a','b'},{10,20,30,40},{7,8,9,10}};
		
		System.out.println(Arrays.deepToString(arr));
		
		for (int [] arr1 :arr){
			for (int ele :arr1){
				System.out.print(ele+" ");
			}
			System.out.println();
		}
	}
}

